                            ARQUITECTURA COLONIAL
                            La Catedral de San Pedro, construida en las últimas décadas del siglo XVI, La primera construcción del templo en 1616 fue realizada por los indios y esclavos, con tierra pisada y techo de paja. El terremoto del 9 de julio de 1766 la arruinó en su totalidad. Se habla de una segunda construcción; inicialmente fue regentada por los padres jesuitas hasta el 17 de agosto de 1767 que fueron desterrados por la pragmática de Carlos III; después de estos acontecimientos entro en total abandono.

                            Años después, con la ayuda económica del Rey de España, fue reconstruida; reconstrucción que se dio por terminada el 30 de diciembre de 1781, para luego ser restaurada en el siglo XX. Con la creación de la Diócesis de Buga, fue elevada al rango de Catedral. El templo, en su patio interior, y las paredes de la capilla del Carmen fueron durante 3 siglos el único cementerio de la ciudad en él descansan los restos de los conquistadores y primeros pobladores de Guadalajara de la Victoria de Buga



